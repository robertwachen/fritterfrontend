      <header>
        <div class="left">
          <h2>
            <span v-if="$store.state.feed == 'Main'">
              Viewing all freets
            </span>
            <span v-else>
              Viewing freets from {{ $store.state.feed }}
            </span>
            <span v-if="$store.state.filter">
